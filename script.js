function renderQuestion() {
  const question = questions[currentQuestion];
  const progress = Math.round((currentQuestion / questions.length) * 100);

  questionCounter.textContent = `Question ${currentQuestion + 1} / ${questions.length}`;
  progressPercent.textContent = `${progress}%`;
  progressFill.style.width = `${progress}%`;
  questionText.textContent = question.text;
  backBtn.style.visibility = currentQuestion === 0 ? 'hidden' : 'visible';

  answersEl.innerHTML = question.answers.map((answer, index) => `
    <button class="answer-btn" data-index="${index}">
      <span class="answer-icon">${answer.icon}</span>
      <span>${answer.text}</span>
    </button>
  `).join('');

  document.querySelectorAll('.answer-btn').forEach(button => {
    button.addEventListener('click', () => chooseAnswer(Number(button.dataset.index)));
  });
}

function chooseAnswer(answerIndex) {
  const answer = questions[currentQuestion].answers[answerIndex];
  history.push(answer);

  for (const [hero, value] of Object.entries(answer.points)) {
    scores[hero] += value;
  }

  currentQuestion += 1;

  if (currentQuestion >= questions.length) {
    showResult();
  } else {
    renderQuestion();
  }
}

function goBack() {
  if (currentQuestion === 0) return;

  const previousAnswer = history.pop();
  for (const [hero, value] of Object.entries(previousAnswer.points)) {
    scores[hero] -= value;
  }

  currentQuestion -= 1;
  renderQuestion();
}

function getWinnerSlug() {
  const entries = Object.entries(scores);
  const highest = Math.max(...entries.map(([, score]) => score));
  const tied = entries.filter(([, score]) => score === highest).map(([slug]) => slug);

  for (let i = history.length - 1; i >= 0; i--) {
    const recentHeroes = Object.keys(history[i].points);
    const found = recentHeroes.find(slug => tied.includes(slug));
    if (found) return found;
  }

  return tied[0];
}

function showResult() {
  progressPercent.textContent = '100%';
  progressFill.style.width = '100%';

  const winnerSlug = getWinnerSlug();
  lastWinnerSlug = winnerSlug;
  const hero = heroes[winnerSlug];

  resultIntroTitle.textContent = 'Un symbole répond à ton âme.';
  resultSymbol.src = hero.symbol;
  resultSymbol.alt = `Symbole de ${hero.name}`;
  resultSymbol.classList.remove('hidden', 'is-fading');

  resultImage.src = hero.portrait;
  resultImage.alt = hero.name;
  resultImage.classList.add('hidden');

  document.querySelector('#resultName').textContent = hero.name;
  document.querySelector('#resultSubtitle').textContent = hero.subtitle;
  document.querySelector('#resultQuote').textContent = `“${hero.quote}”`;
  document.querySelector('#resultDescription').textContent = hero.description;

  document.querySelector('#resultStats').innerHTML = Object.entries(hero.stats).map(([label, value]) => `
    <div class="stat">
      <span>${label}</span>
      <strong>${value}</strong>
    </div>
  `).join('');

  resultDetails.classList.add('hidden');
  discoverBtn.classList.remove('hidden');

  quizSection.classList.add('hidden');
  resultSection.classList.remove('hidden');
  resultSection.scrollIntoView({ behavior: 'smooth' });

  discoverBtn.onclick = () => {
    resultSymbol.classList.add('is-fading');

    window.setTimeout(() => {
      resultSymbol.classList.add('hidden');
      discoverBtn.classList.add('hidden');
      resultIntroTitle.textContent = 'Tu es…';
      resultImage.classList.remove('hidden');
      resultDetails.classList.remove('hidden');
    }, 430);
  };
}

function startQuiz() {
  currentQuestion = 0;
  history = [];
  scores = Object.fromEntries(Object.keys(heroes).map(hero => [hero, 0]));

  document.querySelector('#accueil').classList.add('hidden');
  resultSection.classList.add('hidden');
  quizSection.classList.remove('hidden');

  renderQuestion();
  quizSection.scrollIntoView({ behavior: 'smooth' });
}

function restartQuiz() {
  document.querySelector('#accueil').classList.remove('hidden');
  resultSection.classList.add('hidden');
  quizSection.classList.add('hidden');
  window.scrollTo({ top: 0, behavior: 'smooth' });
}

function openStory(slug) {
  const hero = heroes[slug];
  if (!hero) return;

  storyTitle.textContent = hero.storyTitle;
  storyLead.textContent = hero.storyLead;
  storyPdfLink.href = hero.storyPdf;
  storyPages.innerHTML = hero.storyPages.map((src, index) => `
    <figure class="story-page">
      <img src="${src}" alt="${hero.storyTitle} - page ${index + 1}" loading="lazy">
      <figcaption>Page ${index + 1} / ${hero.storyPages.length}</figcaption>
    </figure>
  `).join('');

  storyOverlay.classList.remove('hidden');
  document.body.classList.add('no-scroll');
  storyOverlay.scrollTo({ top: 0 });
}

function closeStory() {
  storyOverlay.classList.add('hidden');
  document.body.classList.remove('no-scroll');
  storyPages.innerHTML = '';
}

startBtn.addEventListener('click', startQuiz);
backBtn.addEventListener('click', goBack);
restartBtn.addEventListener('click', restartQuiz);
readResultStoryBtn.addEventListener('click', () => {
  if (lastWinnerSlug) openStory(lastWinnerSlug);
});
closeStoryBtn.addEventListener('click', closeStory);
storyOverlay.addEventListener('click', event => {
  if (event.target === storyOverlay) closeStory();
});
storyTopBtn.addEventListener('click', () => storyOverlay.scrollTo({ top: 0, behavior: 'smooth' }));
window.addEventListener('keydown', event => {
  if (event.key === 'Escape' && !storyOverlay.classList.contains('hidden')) closeStory();
});
menuBtn.addEventListener('click', () => navLinks.classList.toggle('is-open'));
navLinks.querySelectorAll('a').forEach(link => link.addEventListener('click', () => navLinks.classList.remove('is-open')));

renderGallery();
renderStories();
